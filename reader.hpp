class Reader
{
    //-----------------------------------------------------------------------------------
public:
    //-----------------------------------------------------------------------------------
    Reader( const string& name );
    //-----------------------------------------------------------------------------------
    const set< Book >& get_books() const;
    //-----------------------------------------------------------------------------------
    const Book& get_book_cref( const string& name_book ) const;
    //-----------------------------------------------------------------------------------
    void add_book( const Book& book );
    //-----------------------------------------------------------------------------------
    Book get_book( const string& name_book );
    //-----------------------------------------------------------------------------------
    bool contain( const string& name_book );
    //-----------------------------------------------------------------------------------
    const string& get_name() const;
    //-----------------------------------------------------------------------------------
private:
    //-----------------------------------------------------------------------------------
    set< Book > taken_books;
    string      name;
    //-----------------------------------------------------------------------------------
};
/////////////////////////////////////////////////////////////////////////////////////////
inline bool operator==( const Reader& r1, const Reader& r2 )
{
    return r1.get_name() == r2.get_name();
}
//---------------------------------------------------------------------------------------
inline bool operator==( const Reader& reader, const string& name )
{
    return reader.get_name() == name;
}
